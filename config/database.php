<?php

require_once __DIR__ . '/../vendor/autoload.php';

use Dotenv\Dotenv;

/*
|--------------------------------------------------------------------------
| LOAD ENV
|--------------------------------------------------------------------------
*/

$dotenv = Dotenv::createImmutable(
    __DIR__ . '/../'
);

$dotenv->load();

/*
|--------------------------------------------------------------------------
| DATABASE CONNECTION
|--------------------------------------------------------------------------
*/

$conn = mysqli_connect(
    $_ENV['DB_HOST'],
    $_ENV['DB_USER'],
    $_ENV['DB_PASS'],
    $_ENV['DB_NAME']
);

if (!$conn) {

    die("Database Connection Failed");

}

/*
|--------------------------------------------------------------------------
| RUN MIGRATIONS
|--------------------------------------------------------------------------
*/

include(__DIR__ . '/../database/migrations.php');

/*
|--------------------------------------------------------------------------
| RUN SEEDERS
|--------------------------------------------------------------------------
*/

include(__DIR__ . '/../database/seeders.php');